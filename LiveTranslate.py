def _extract_package():
    """Unpack the embedded 'livetranslate' package into the lib dir.

    Re-extracts when this file carries a different version than the one
    on disk, so updating the plugin is just replacing this file.
    """
    stamp = os.path.join(_LIB_DIR, "livetranslate", ".version")
    try:
        with open(stamp, "r", encoding="utf-8") as fh:
            if fh.read().strip() == __version__:
                return
    except OSError:
        pass
    os.makedirs(_LIB_DIR, exist_ok=True)
    data = base64.b64decode("".join(_PAYLOAD))
    with zipfile.ZipFile(io.BytesIO(data)) as zf:
        zf.extractall(_LIB_DIR)
    with open(stamp, "w", encoding="utf-8") as fh:
        fh.write(__version__)


def main():
    _extract_package()
    if _LIB_DIR not in sys.path:
        sys.path.insert(0, _LIB_DIR)
    from livetranslate.bootstrap import ensure_ready

    try:
        script_path = os.path.abspath(__file__)
    except NameError:  # some hosts exec without __file__
        script_path = None
    ensure_ready(script_path=script_path, resolve=globals().get("resolve"))


if __name__ == "__main__" or globals().get("resolve") is not None:
    main()
